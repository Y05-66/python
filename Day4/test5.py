def list_while_fun():
    my_list = [1,2,3,4,5]
    index = 0
    while index < len(my_list):
        element = my_list[index]
        print(element)
        index += 1

def list_for_fun():
    my_list = [1,2,3,4,5]
    for element in my_list:
        print(element)

